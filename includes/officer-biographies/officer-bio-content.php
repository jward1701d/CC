                    <p>David was raised in a family with an entrepreneurial spirit. His father started and successfully grew his own rotary drilling business for over 30 years, as did his grandfather and  various uncles.  His brother started and ran his own timber business. David was fortunate to experience and work in both fields; drilling for and with his father and the timber business with his brother.</p>
                <p>The one common theme both these business shared was their being connected to the coal industry, both directly and indirectly. His father worked for various coal companies throughout the Appalachian area for many years. His brother contracted timber sales for Massey Energy in the 1980s.</p>
                <p>Of all the different job sites he traveled to with his father, David enjoyed working for and was most intrigued with the coal industry. When James Hager asked him to spear head a new concept, his personal dream, Coalfield Connection, David was all too eager to accept the challenge.</p>
                <p>In August 2009, David and his partners began researching and building the foundation for what would ultimately become Coalfield Connection and CoalfieldConnection.com, the yellow pages for everything needed in the mine industry.  Their goal was to have a physical literal directory in hand and on line by November of 2009.</p>
                <p>In September David hit the road and began selling the concept to various mine-related vendors. The idea of an online directory for the coal/mining industry, mailed to all mines nationwide and an ever-increasing list of vendors, quickly caught on and continues to grow both on a national and international level!</p>
                <p>David has been instrumental in growing what was just a concept into what is now a vital link for all vendors out there who sell and service the coal and/or mining industry. We are always growing, thinking and striving to be the best and most user-friendly informational publication for the mining industry.</p>
                
